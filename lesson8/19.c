#include <stdio.h>

// Рекурсивная функция для подсчета количества символов 'a' в строке
int acounter(void) {
    char c;
    scanf("%c", &c); // Считываем один символ из ввода

    if (c == '.') { // Базовый случай: если символ - точка, прекращаем рекурсию
        return 0;
    }

    if (c == 'a') { // Если символ равен 'a', увеличиваем счетчик
        return 1 + acounter();
    } else {
        return acounter(); // Иначе продолжаем рекурсию без увеличения счетчика
    }
}

int main() {
    int count = acounter(); // Вызываем рекурсивную функцию для подсчета символов 'a'
    printf("%d\n", count);  // Выводим количество символов 'a'
    return 0;
}
